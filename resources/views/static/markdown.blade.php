<h1 style="text-align:center;">Markdown Průvodce</h1>
<p>Markdown je odlehčený značkovací jazyk, který slouží pro úpravu prostého textu. Více na <a href="https://cs.wikipedia.org/wiki/Markdown" target="_blank">Wikipedii</a> Do textu jsou přidávány znaky jako <code>#</code>, <code>*</code>, <code>-</code> apod., které jsou následně odstraněny a text je naformátován.<br>Opravdový výsledek si lze zobrazit v náhledu, pod ikonkou <code><i class="fa fa-eye"></i></code>.</p>

<h4>Označení</h4>
<pre><code style="min-width: 100%;">**<strong>tučně</strong>**
*<em>kurzíva</em>*
~~<strike>přeskrtnuté</strike>~~</code></pre>

<h4>Nadpisy</h4>
<pre><code style="min-width: 100%;"># Nadpis úrovně 1
## Nadpis úrovně 2
### Nadpis úrovně 3
#### Nadpis úrovně 4
##### Nadpis úrovně 5
###### Nadpis úrovně 6
</code></pre>

